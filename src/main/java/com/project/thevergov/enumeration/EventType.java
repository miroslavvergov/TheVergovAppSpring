package com.project.thevergov.enumeration;

/**
 * EventType: An enumeration representing various types of events within the application.
 * This enum is used to categorize and manage different kinds of events that the application handles.
 */
public enum EventType {

    /**
     * REGISTRATION: Represents an event where a user registers a new account within the application.
     */
    REGISTRATION,

    /**
     * RESETPASSWORD: Represents an event where a user initiates a password reset process.
     */
    RESETPASSWORD;
}
